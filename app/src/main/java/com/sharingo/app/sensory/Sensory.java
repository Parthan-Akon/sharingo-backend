package com.sharingo.app.sensory;

public class Sensory {

	private String recipeName;
	private int foodCallRecordId;
	private int appearance;
	private int aroma;
	private int taste;
	private int temperature;
	private String fitStatus;
	private String acceptability;
	public String getRecipeName() {
		return recipeName;
	}
	public void setRecipeName(String recipeName) {
		this.recipeName = recipeName;
	}
	public int getFoodCallRecordId() {
		return foodCallRecordId;
	}
	public void setFoodCallRecordId(int foodCallRecordId) {
		this.foodCallRecordId = foodCallRecordId;
	}
	public int getAppearance() {
		return appearance;
	}
	public void setAppearance(int appearance) {
		this.appearance = appearance;
	}
	public int getAroma() {
		return aroma;
	}
	public void setAroma(int aroma) {
		this.aroma = aroma;
	}
	public int getTaste() {
		return taste;
	}
	public void setTaste(int taste) {
		this.taste = taste;
	}
	public int getTemperature() {
		return temperature;
	}
	public void setTemperature(int temperature) {
		this.temperature = temperature;
	}
	public String getFitStatus() {
		return fitStatus;
	}
	public void setFitStatus(String fitStatus) {
		this.fitStatus = fitStatus;
	}
	public String getAcceptability() {
		return acceptability;
	}
	public void setAcceptability(String acceptability) {
		this.acceptability = acceptability;
	}
	
	
	
	
}
